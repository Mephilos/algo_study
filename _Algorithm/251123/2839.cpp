#include <iostream>
#include <vector>

using namespace std;

int main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);

    int N;
    cin >> N;
    int cnt = 0;

    while (N >= 0)
    {
        if (N % 5 == 0)
        {
            cnt += (N /= 5);
            cout << cnt << '\n';
            return 0;
        }

        N-= 0;
        cnt++;
    }

    cout << -1 << '\n';
}