#include <iostream>

int main() {
    // 음, 고양이 그림을 출력하는 거네.
    // 백슬래시(\)는 그냥 쓰면 안되고, 두 번 써야(\\) 하나가 출력되지.
    // 잊지 말고 잘 처리해주자.
    
    std::cout << "\\    /\\" << std::endl;
    std::cout << " )  ( ')" << std::endl;
    std::cout << "(  /  )" << std::endl;
    std::cout << " \\(__)|" << std::endl;
    
    return 0;
}