#include <iostream>

using namespace std;

int main()
{
    int num1, num2, result;

    cin >> num1 >> num2;
    result = num1 + num2; 
    cout << result << endl;
    result = num1 - num2; 
    cout << result << endl;
    result = num1 * num2; 
    cout << result << endl;
    result = num1 / num2; 
    cout << result << endl;
    result = num1 % num2; 
    cout << result << endl;
}